            const scrollWhatCanCreateTabs = (button) => {
                const filterSection = button.closest('section');
                const activeTab = filterSection.querySelector('.studprojtab.active');
                const progtabsContainer = filterSection.querySelector('#studprojtabs');
                const isNext = button.classList.contains('what_you_can_create_menu__arrow_next');
                let nextTab = isNext ? activeTab.nextElementSibling : activeTab.previousElementSibling;

                if (!nextTab || !nextTab.classList.contains('studprojtab')) {
                    nextTab = filterSection.querySelector(`.studprojtab:${isNext ? 'first-of-type' : 'last-of-type'}`);
                }
                const tabRect = nextTab.getBoundingClientRect();
                const containerRect = progtabsContainer.getBoundingClientRect();
                const scrollOffset = tabRect.left - containerRect.left + progtabsContainer.scrollLeft;

                progtabsContainer.scrollTo({
                    left: scrollOffset,
                    behavior: 'smooth'
                });

                const filterData = `${nextTab.id}-content`;

                filterSection.querySelector('.active').classList.remove('active');
                nextTab.classList.add('active');
                const allPrograms = filterSection.querySelectorAll('.studproj-cat-list')?.forEach(el => {
                    el.classList.remove('active')
                });
                filterSection.querySelector(`#${filterData}`).classList.add('active');
            };

            document.querySelectorAll('.what_you_can_create_menu__arrow_previous, .what_you_can_create_menu__arrow_next').forEach(button => {
